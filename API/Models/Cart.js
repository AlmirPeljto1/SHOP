//IMPORT
const mongoose = require("mongoose");
//SCHEMA
const CartSchema = new mongoose.Schema(
  {
    userId: {
      type: String,
      required: true,
    },
    products: [
      {
        productId: {
          type: String,
        },
        quantity: {
          type: Number,
          default: 1,
        },
      },
    ],
  },
  { timestamps: true }
);
//EXPORT
module.exports = mongoose.model("Cart", CartSchema);
